
//lightbox 1

var lightbox_link_1 = document.getElementById('site1_link');
var lightbox_1 = document.getElementById('site1_lightbox');
var exit_1 = document.getElementById('exit_1');

lightbox_link_1.addEventListener('click', e => {
        lightbox_1.classList.add('active');
        document.body.style.overflow = 'hidden'

})

exit_1.addEventListener('click', e => {
    lightbox_1.classList.remove('active');
    document.body.style.overflow = 'scroll'
})

